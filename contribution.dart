class Contribution {
  final String name;
  final String id;
  final String postPicUrl;
  final int like;
  final int comment;
  final int share;
  final bool favorit;
  final String info;
  final String link;

  Contribution({
    required this.name,
    required this.id,
    required this.postPicUrl,
    required this.like,
    required this.comment,
    required this.share,
    required this.favorit,
    required this.info,
    required this.link,
  });
}
