class Contribution {
  final String name;
  final String id;
  final String profilePicUrl;
  final String postPicUrl;
  final int likee;
  final String commentt;
  final int sharee;
  final bool favoritt;
  final String info;
  final String link;

  Contribution({
    required this.name,
    required this.id,
    required this.profilePicUrl,
    required this.postPicUrl,
    required this.likee,
    required this.commentt,
    required this.sharee,
    required this.favoritt,
    required this.info,
    required this.link,
  });
}
