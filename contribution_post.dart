class Contribution {
  String name;
  String id;
  String postPicUrl;
  int like;
  int comment;
  int share;
  bool favorit;
  String info;
  String link;

  Contribution({
    required this.name,
    required this.id,
    required this.postPicUrl,
    required this.like,
    required this.comment,
    required this.share,
    required this.favorit,
    required this.info,
    required this.link,
  });
}
